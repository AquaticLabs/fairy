package io.fairyproject.gradle.dependency

import io.fairyproject.gradle.PluginApplicationAction
import io.fairyproject.gradle.constants.CoordinateConstants
import io.spring.gradle.dependencymanagement.DependencyManagementPlugin
import io.spring.gradle.dependencymanagement.dsl.DependencyManagementExtension
import org.gradle.api.Project

class DependencyManagementPluginAction : PluginApplicationAction {

    override val pluginClass = DependencyManagementPlugin::class.java

    override fun execute(project: Project) {
        project.extensions.findByType(DependencyManagementExtension::class.java)!!.imports {
            it.mavenBom(CoordinateConstants.bom)
        }
    }

}